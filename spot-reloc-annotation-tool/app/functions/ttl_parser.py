"""
ttl_parser.py — validate TTL and sync changed values back into the spaces dict.

Strategy:
  • Validate syntax with rdflib (returns error string on failure).
  • Match TTL subjects to existing spaces by their IRI local name
    (the same "safe name" generated by generate_rdf).
  • Sync back: rdfs:label → name, rdfs:comment → comment,
    BPM coordinate values for DocumentSpace / PointSpace,
    _Start / _End point coordinates for LineSpace.
  • Coordinates in the TTL are stored in the same coordinate frame as
    the space JSON (absolute 0..1 normalized, relative to the whole doc image),
    so no transformation is needed.
"""

from __future__ import annotations
import copy
import re
from typing import Dict, Optional, Tuple

import rdflib
from rdflib import RDF, RDFS, Namespace

SPOT    = Namespace("https://w3id.org/spot#")
SPOT_AM = Namespace("https://w3id.org/spot/am#")
EX      = Namespace("http://example.org/")


def _safe(name: str) -> str:
    s = str(name).strip() or "Space"
    s = re.sub(r'[\s\x00-\x1f"#<>{}|\\\^`\[\]/]', '_', s)
    return s or "Space"


def validate_and_parse_ttl(
    ttl: str,
    existing_spaces: Dict,
) -> Tuple[Optional[str], Optional[Dict]]:
    """
    Returns (error_string, updated_spaces).
    If parsing succeeds: error_string is None, updated_spaces is the updated copy.
    If parsing fails:    error_string describes the problem, updated_spaces is None.
    """
    # ── 1. Syntax validation ───────────────────────────────────────────
    g = rdflib.Graph()
    try:
        g.parse(data=ttl, format="turtle")
    except Exception as exc:
        # Clean up rdflib's verbose error
        msg = str(exc).splitlines()[0]
        return f"TTL syntax error: {msg}", None

    # ── 2. Build safe-name → space-id reverse map ─────────────────────
    safe_to_sid: Dict[str, str] = {}
    for sid, sp in existing_spaces.items():
        safe_to_sid[_safe(sp.get("name", sid))] = sid

    updated = copy.deepcopy(existing_spaces)

    # ── 3. Helper: extract float literal value ─────────────────────────
    def _coord(subject, predicate) -> Optional[float]:
        for obj in g.objects(subject, predicate):
            try:
                return float(str(obj))
            except (ValueError, TypeError):
                pass
        return None

    # ── 4. Iterate over known space types ─────────────────────────────
    TYPE_MAP = {
        SPOT.DocumentSpace: "DocumentSpace",
        SPOT.PointSpace:    "PointSpace",
        SPOT.LineSpace:     "LineSpace",
    }

    for rdf_class, sp_type in TYPE_MAP.items():
        for subj in g.subjects(RDF.type, rdf_class):
            # Derive local name from full IRI
            iri_str = str(subj)
            local = iri_str.split("#")[-1].split("/")[-1]

            if local not in safe_to_sid:
                continue  # unknown subject (class definitions, asset space, doc space…)

            sid = safe_to_sid[local]
            sp  = updated[sid]

            # ── label → name ──────────────────────────────────────────
            for label in g.objects(subj, RDFS.label):
                sp["name"] = str(label)
                break

            # ── comment ───────────────────────────────────────────────
            comments = list(g.objects(subj, RDFS.comment))
            if comments:
                sp["comment"] = str(comments[0])
            # If comment triple is removed, keep whatever was there (don't wipe it)

            # ── coordinates ───────────────────────────────────────────
            if sp_type == "DocumentSpace":
                bbox = dict(sp.get("bbox") or {"left": 0, "top": 0, "right": 1, "bottom": 1})
                coord_pred = SPOT_AM.hasNormalizedCoordinateValue
                for suffix, key in [
                    ("X_Min", "left"), ("X_Max", "right"),
                    ("Y_Min", "top"), ("Y_Max", "bottom"),
                ]:
                    bpm_iri = EX[f"BPM_{local}_{suffix}"]
                    v = _coord(bpm_iri, coord_pred)
                    if v is not None:
                        bbox[key] = round(v, 6)
                sp["bbox"] = bbox

            elif sp_type == "PointSpace":
                pt = dict(sp.get("point") or {"x": 0.5, "y": 0.5, "z": 0.5})
                coord_pred = SPOT_AM.hasNormalizedCoordinateValue
                for suffix, key in [("X", "x"), ("Y", "y"), ("Z", "z")]:
                    bpm_iri = EX[f"BPM_{local}_{suffix}"]
                    v = _coord(bpm_iri, coord_pred)
                    if v is not None:
                        pt[key] = round(v, 6)
                sp["point"] = pt

            elif sp_type == "LineSpace":
                s_pt = dict(sp.get("start") or {"x": 0.0, "y": 0.5, "z": 0.5})
                e_pt = dict(sp.get("end")   or {"x": 1.0, "y": 0.5, "z": 0.5})
                start_iri = EX[f"{local}_Start"]
                end_iri   = EX[f"{local}_End"]
                for coord_pred, key in [
                    (SPOT_AM.hasNormalizedCoordinateX, "x"),
                    (SPOT_AM.hasNormalizedCoordinateY, "y"),
                    (SPOT_AM.hasNormalizedCoordinateZ, "z"),
                ]:
                    v = _coord(start_iri, coord_pred)
                    if v is not None:
                        s_pt[key] = round(v, 6)
                    v = _coord(end_iri, coord_pred)
                    if v is not None:
                        e_pt[key] = round(v, 6)
                sp["start"] = s_pt
                sp["end"]   = e_pt

    return None, updated
